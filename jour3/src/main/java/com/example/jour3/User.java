package com.example.jour3;

public class User {
    private String name;
    private int age;

    //Getters and setters
    public String getName() {
        return name;
    }
    public int getAge() {
        return age;
    }
    public void setName(String newName) {
        this.name = newName;
    }
    public void setAge(int newAge) {
        this.age = newAge;
    }
}
